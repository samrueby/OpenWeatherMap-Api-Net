using System.Xml.Serialization;

namespace OpenWeatherMap {
	/// <summary>
	/// Class WindSpeed.
	/// </summary>
	public class WindSpeed {
		/// <summary>
		/// Gets or sets the speed (MPS).
		/// </summary>
		/// <value>The MPS.</value>
		[ XmlAttribute( "mps" ) ]
		public double Mps { get; set; }

		/// <summary>
		/// Gets or sets the name.
		/// </summary>
		/// <value>The name.</value>
		[ XmlAttribute( "name" ) ]
		public string Name { get; set; }
	}
}
